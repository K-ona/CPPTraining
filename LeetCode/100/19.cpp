// Definition for singly-linked list.
struct ListNode {
  int val;
  ListNode* next;
  ListNode() : val(0), next(nullptr) {}
  ListNode(int x) : val(x), next(nullptr) {}
  ListNode(int x, ListNode* next) : val(x), next(next) {}
};

class Solution {
 public:
  ListNode* removeNthFromEnd(ListNode* head, int n) {
    ListNode* pre = head;
    int cnt = 1;
    while (head->next) {
      head = head->next;
      ++cnt;
    }
    head = pre;
    cnt = cnt - n - 1;
    while (cnt-- > 0 and pre->next) {
      pre = pre->next;
    }
    cnt == -1 ? (pre->next ? (pre->next = pre->next->next) : nullptr)
              : (head = head->next);
    return head;
  }
};

