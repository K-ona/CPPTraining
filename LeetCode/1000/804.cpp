class Solution {
 public:
  int uniqueMorseRepresentations(vector<string>& words) {
    std::unordered_map<string, int> M; 
    for (auto &word: words) {
      string morse = "";
      for (auto c: word) {
        morse += arr[c - 'a'];
      }
      ++M[morse];      
    }
    return M.size();
  }
};

